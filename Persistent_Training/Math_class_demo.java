
public class Math_class_demo {
   
    public static void main(String[] args) {
        
    
    //Math class is having this static methods, so we can call directly using classname
    System.out.println(Math.abs(-26.7));
    System.out.println(Math.ceil(7.4));
    System.out.println(Math.floor(7.4));

    System.out.println(Math.max(327,100));
    System.out.println(Math.min(7.5,100));
    System.out.println(Math.pow(5,3));
    System.out.println(Math.sqrt(9));
        
}
}
